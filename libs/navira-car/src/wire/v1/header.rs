use crate::wire::{CarDeserializable, cid::RawCid, varint::UnsignedVarint};
use cbor4ii::core::{Value, dec::Decode as _, utils::SliceReader};

pub struct CarV1Header {
    version: u64,
    roots: Vec<RawCid>,
}

impl CarV1Header {
    pub fn new(roots: Vec<RawCid>) -> Self {
        CarV1Header { roots, version: 1 }
    }

    pub fn version(&self) -> u64 {
        self.version
    }

    pub fn roots(&self) -> &[RawCid] {
        &self.roots
    }
}

